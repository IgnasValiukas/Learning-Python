# Programoje išbandyti daugiau string funkcijų:
# upper()
# casefold()
# capitalize()
# count()
# find() ir t.t.
line = "I love apples, apple are my favorite fruit"

print("Upper:", line.upper(), )
print("Casefold:", line.casefold())
print("Capitalize:", line.capitalize())
print("Find:", line.find("love"), "Count:", line.count("apple"))